package com.example.cfs2.servlet.beans;

public class Generi {
String nome;

public Generi() {
	this.nome = nome;
}

public Generi(String nome) {
	this.nome = nome;
}

public String getGeneri() {
	return nome;
}
public void setGeneri(String nome) {
	this.nome = nome;
}

@Override
public String toString() {
	return nome;
}
}
